class DriverApp extends StatefulWidget {
  const DriverApp({super.key});

  @override
  State<DriverApp> createState() => _DriverAppState();
}

class _DriverAppState extends State<DriverApp> {
  Widget _initialRoute = const LoginPage();
  bool _isLoading = true;

  @override
  void initState() {
    super.initState();
    _checkAuthState();
  }

  Future<void> _checkAuthState() async {
    try {
      final token = await StorageService.getToken();
      final driverId = await StorageService.getDriverId();
      
      if (token != null && token.isNotEmpty && driverId != null && driverId.isNotEmpty) {
        // Token exists, navigate to main screen
        setState(() {
          _initialRoute = MainScreen(token: token, driverId: driverId);
          _isLoading = false;
        });
      } else {
        // No token, show login page
        setState(() {
          _initialRoute = const LoginPage();
          _isLoading = false;
        });
      }
    } catch (e) {
      // On error, show login page
      setState(() {
        _initialRoute = const LoginPage();
        _isLoading = false;
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    if (_isLoading) {
      return MaterialApp(
        debugShowCheckedModeBanner: false,
        home: Scaffold(
          body: Center(child: CircularProgressIndicator()),
        ),
      );
    }
    
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      title: 'Driver Partner',
      theme: ThemeData(
        colorScheme: ColorScheme.fromSeed(seedColor: const Color(0xFF2575FC)),
        useMaterial3: true,
        scaffoldBackgroundColor: const Color(0xFFF8F9FD),
        appBarTheme: const AppBarTheme(
          backgroundColor: Colors.white,
          surfaceTintColor: Colors.white,
          centerTitle: true,
          elevation: 0,
          titleTextStyle: TextStyle(
            color: Colors.black,
            fontSize: 18,
            fontWeight: FontWeight.w600,
          ),
          iconTheme: IconThemeData(color: Colors.black),
        ),
      ),
      home: _initialRoute,
    );
  }
}

class LoginPage extends StatefulWidget {
  const LoginPage({super.key});

  @override
  State<LoginPage> createState() => _LoginPageState();
}

class _LoginPageState extends State<LoginPage> {
  final _phoneController = TextEditingController(text: '9944226010');
  final String _defaultName = 'Driver Tester';
  final String _defaultEmail = 'driver@test.com';
  final _otpController = TextEditingController();

  bool _loading = false;
  String? _otpHint;
  bool _useLoginFlow = true;
  String? _smsToken;
  String? _driverId;

  final _api = ApiClient(baseUrl: kApiBaseUrl);

  @override
  void dispose() {
    _phoneController.dispose();
    _otpController.dispose();
    super.dispose();
  }

  String _cleanPhone() {
    // Remove all non-digits and return clean phone number
    // Database stores phones without country code (e.g., 9944226010)
    final digits = _phoneController.text.replaceAll(RegExp(r'[^0-9]'), '');
    // Remove country code if present (91 prefix)
    if (digits.startsWith('91') && digits.length > 10) {
      return digits.substring(2); // Remove '91' prefix
    }
    return digits;
  }

  Future<void> _requestOtp() async {
    setState(() {
      _loading = true;
      _otpHint = null;
    });
    try {
      // Send phone without country code - backend will normalize it
      final phone = _cleanPhone();

      // Try login first (most users are existing drivers)
      try {
        final loginRes = await _api.sendLoginOtp(phone: phone);
        
        // If driver not found (404), throw to trigger signup flow
        if (loginRes.statusCode == 404) {
          throw Exception('Driver not found');
        }

        // Check if request was successful
        if (!loginRes.success) {
          throw Exception(loginRes.message.isNotEmpty 
              ? loginRes.message 
              : 'Failed to send OTP');
        }

        // Use body getter for type safety (ensures Map<String, dynamic>)
        final loginData = loginRes.body;
        final loginOtp = loginData['otp']?.toString();
        _smsToken = loginData['smsToken']?.toString();
        _driverId = loginData['driverId']?.toString();
        
        // Debug: Print response to help diagnose issues
        print('Login OTP Response: $loginData');
        print('SMS Token: $_smsToken');
        print('Driver ID: $_driverId');
        
        if (loginOtp != null && loginOtp.isNotEmpty) {
          _otpController.text = loginOtp;
          _otpHint = 'OTP (for testing): $loginOtp';
        }
        _useLoginFlow = true;
      } catch (e) {
        // If login fails (driver not found), try signup
        final errorMsg = e.toString().toLowerCase();
        if (errorMsg.contains('404') || errorMsg.contains('not found')) {
          // Clear any previous OTP to avoid confusion
          _otpController.clear();
          
          final signupRes = await _api.sendSignupOtp(
            phone: phone,
            name: _defaultName,
            email: _defaultEmail,
          );
          
          // Use body getter for type safety (ensures Map<String, dynamic>)
          final data = signupRes.body;
          final otp = data['otp']?.toString();
          _smsToken = data['smsToken']?.toString();
          _driverId = data['driverId']?.toString();
          
          // Debug: Print response to help diagnose issues
          print('Signup OTP Response: $data');
          print('SMS Token: $_smsToken');
          print('Driver ID: $_driverId');
          
          if (otp != null && otp.isNotEmpty) {
            _otpController.text = otp;
            _otpHint = 'OTP (for testing): $otp';
          }
          _useLoginFlow = false;
        } else {
          // Some other error, rethrow
          rethrow;
        }
      }
    } catch (e) {
      _showError(e);
    } finally {
      setState(() {
        _loading = false;
      });
    }
  }

  Future<void> _verifyOtp() async {
    setState(() {
      _loading = true;
    });
    try {
      // Use clean phone without country code - backend will normalize it
      final phone = _cleanPhone();
      final otpVal = _otpController.text.trim();

      ApiResult result;
      if (_useLoginFlow) {
        if (_smsToken == null) {
          throw Exception('SMS token not found. Please request OTP again.');
        }
        result = await _api.verifyLoginOtp(
          phone: phone,
          otp: otpVal,
          smsToken: _smsToken!,
          driverId: _driverId,
        );
        if (!result.success) {
          throw Exception(result.body['message'] ?? 'Login failed');
        }
      } else {
        if (_smsToken == null) {
          throw Exception('SMS token not found. Please request OTP again.');
        }
        // Get FCM token for signup verification (required by backend)
        String? fcmToken;
        try {
          fcmToken = await FcmService().getToken();
        } catch (e) {
          debugPrint('Error getting FCM token for signup: $e');
          // Use empty string if FCM token unavailable (backend requires it)
          fcmToken = '';
        }
        
        result = await _api.verifySignupOtp(
          phone: phone,
          otp: otpVal,
          smsToken: _smsToken!,
          fcmToken: fcmToken ?? '', // Backend requires this field
          name: _defaultName,
          email: _defaultEmail,
        );

        final msg = (result.body['message'] ?? '').toString().toLowerCase();
        if (!result.success && msg.contains('already exists')) {
          _useLoginFlow = true;
          // Re-request login OTP to get smsToken
          final loginRes = await _api.sendLoginOtp(phone: phone);
          if (loginRes.statusCode == 404) {
            throw Exception('Driver not found');
          }
          // Use body getter for type safety (ensures Map<String, dynamic>)
          final loginData = loginRes.body;
          _smsToken = loginData['smsToken']?.toString();
          _driverId = loginData['driverId']?.toString();
          
          // Debug: Print response to help diagnose issues
          print('Re-request Login OTP Response: $loginData');
          print('SMS Token: $_smsToken');
          
          if (_smsToken == null) {
            throw Exception('SMS token not found. Please request OTP again.');
          }
          result = await _api.verifyLoginOtp(
            phone: phone,
            otp: otpVal,
            smsToken: _smsToken!,
            driverId: _driverId,
          );
          if (!result.success) {
            throw Exception(result.body['message'] ?? 'Login failed');
          }
        } else if (!result.success) {
          throw Exception(result.body['message'] ?? 'Signup failed');
        }
      }

      // Backend returns: { success: true, message: "...", data: { driver: {...}, token: "...", refreshToken: "..." } }
      // Use body getter for type safety, then access the nested 'data' field
      final responseBody = result.body;
      final data = responseBody['data'] as Map<String, dynamic>? ?? {};
      
      // Debug: Print response to help diagnose issues
      print('========================================');
      print('Verify OTP Response: $responseBody');
      print('Data: $data');
      print('========================================');
      
      final token = data['token'] as String? ?? data['accessToken'] as String?;
      print('Token extracted: ${token != null ? "Found" : "Missing"}');
      if (token == null) {
        print('‚ùå ERROR: Token is null!');
        print('Full data keys: ${data.keys.toList()}');
        print('Response body keys: ${responseBody.keys.toList()}');
      }

      if (token != null && mounted) {
        final driver = (data['driver'] as Map<String, dynamic>?) ?? {};
        final driverId = driver['driverId']?.toString() ?? 'unknown';
        final refreshToken = data['refreshToken'] as String?;
        
        // Save token and driver data to local storage
        await StorageService.saveToken(token);
        if (refreshToken != null) {
          await StorageService.saveRefreshToken(refreshToken);
        }
        await StorageService.saveDriverId(driverId);
        await StorageService.saveDriverData(driver);
        await StorageService.savePhone(_phoneController.text.trim());
        
        print('Driver data: $driver');
        print('Driver keys: ${driver.keys.toList()}');
        
        // Driver model uses 'adminVerified' field: "Pending", "Approved", or "Rejected"
        // Backend returns "Approved" (capital A), convert to lowercase for comparison
        final adminVerifiedRaw = driver['adminVerified']?.toString() ?? '';
        final adminVerified = adminVerifiedRaw.toLowerCase();
        
        // Check document verification status
        final documentVerified = driver['documentVerified']?.toString().toLowerCase() ?? '';
        final profileVerified = driver['profileVerified']?.toString().toLowerCase() ?? '';
        
        // Debug: Print driver status
        print('========================================');
        print('Admin Verified (raw): "$adminVerifiedRaw"');
        print('Admin Verified (normalized): "$adminVerified"');
        print('Document Verified: "$documentVerified"');
        print('Profile Verified: "$profileVerified"');
        print('========================================');
        
        // Check if driver is approved (case-insensitive)
        // Also check if documents are accepted as fallback for KYC approved drivers
        final isApproved = adminVerified == 'approved' || 
                          adminVerifiedRaw == 'Approved' ||
                          (adminVerified.isEmpty && documentVerified == 'accepted' && profileVerified == 'accepted');
        
        if (isApproved) {
          print('‚úÖ Driver approved (adminVerified: "$adminVerifiedRaw") - navigating to MainScreen');
          // Request permissions before navigating to main screen
          await _requestPermissions(context);
          
          // Update FCM token
          try {
            final fcmToken = await FcmService().getToken();
            if (fcmToken != null) {
              debugPrint('FCM Token to update: $fcmToken');
              // Send token to backend
              try {
                final updateResult = await _api.updateFCMToken(token: token, fcmToken: fcmToken);
                if (updateResult.success) {
                  debugPrint('‚úÖ FCM token updated successfully on backend');
                } else {
                  debugPrint('‚ö†Ô∏è Failed to update FCM token: ${updateResult.body}');
                }
              } catch (e) {
                debugPrint('‚ö†Ô∏è Error updating FCM token on backend: $e');
                // Don't block login if FCM update fails
              }
            }
          } catch (e) {
            debugPrint('Error getting FCM token: $e');
          }

          if (mounted) {
            print('üöÄ Navigating to MainScreen...');
            Navigator.of(context).pushAndRemoveUntil(
              MaterialPageRoute(
                builder: (_) => MainScreen(
                  token: token,
                  driverId: driverId,
                ),
              ),
              (route) => false,
            );
            print('‚úÖ Navigation completed');
          } else {
            print('‚ùå Widget not mounted, cannot navigate');
          }
        } else if (adminVerified == 'rejected') {
          print('‚ùå Account rejected - navigating to WaitingPage');
          Navigator.of(context).push(
            MaterialPageRoute(
              builder: (_) => const WaitingPage(
                title: 'Account Rejected',
                message: 'Your account has been rejected. Please contact support.',
              ),
            ),
          );
        } else {
          print('‚è≥ Account pending - checking document status');
          // Driver status is pending - check document verification status
          if (documentVerified == 'accepted' || profileVerified == 'accepted') {
            print('üìÑ Documents verified - navigating to WaitingPage (Under Review)');
            // Documents submitted and verified, waiting for admin approval
            Navigator.of(context).push(
              MaterialPageRoute(
                builder: (_) => const WaitingPage(
                  title: 'Under Review',
                  message: 'Your documents have been submitted and are under admin review.\n\nYou will be notified once approved.',
                ),
              ),
            );
          } else {
            print('üìù Documents not verified - navigating to WelcomeScreen (KYC)');
            // Documents not submitted yet (pending/empty) - go to KYC flow
            Navigator.of(context).push(
              MaterialPageRoute(
                builder: (_) => WelcomeScreen(token: token),
              ),
            );
          }
        }
      } else if (token == null) {
        print('‚ùå ERROR: Token is null, cannot proceed');
        print('Response body: $responseBody');
        throw Exception(result.body['message'] ?? 'Token missing');
      } else if (!mounted) {
        print('‚ùå ERROR: Widget not mounted, cannot navigate');
      }
    } catch (e) {
      _showError(e);
    } finally {
      setState(() {
        _loading = false;
      });
    }
  }

  Future<void> _requestPermissions(BuildContext context) async {
    try {
      final messenger = ScaffoldMessenger.maybeOf(context);
      final permissionService = PermissionService();
      final results = await permissionService.requestPermissionsWithDialog(context);
      
      if (!mounted || messenger == null) return;

      final missing = <String>[];
      if (!results['location']!) missing.add('Location');
      if (!results['overlay']!) missing.add('Overlay');
      
      if (missing.isNotEmpty) {
        messenger.showSnackBar(
          SnackBar(
            content: Text('Some permissions are missing: ${missing.join(", ")}. Some features may not work.'),
            duration: const Duration(seconds: 4),
          ),
        );
      }
    } catch (e) {
      debugPrint('Error requesting permissions: $e');
      // Don't block login if permission request fails
    }
  }

  void _showError(Object e) {
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(content: Text(e.toString())),
    );
  }

